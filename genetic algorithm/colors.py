def getColor(color):
    if(color == 'WHITE'):
        return (255,255,255)
    elif(color == 'BLACK'):
        return (0,0,0)
    elif(color == 'RED'):
        return (255,0,0)
    elif(color == 'GREEN'):
        return (0,255,0)
    elif(color == 'BLUE'):
        return (0,0,255)
    elif(color == 'ORANGE'):
        return (255,165,0)
    elif(color == 'YELLOW'):
        return (255,255,0)
    elif(color == 'PURPLE'):
        return (160,32,240)
